// Transfers the local buffer to the CGRAM in the SSD1306
void transferBuffer(uint8_t *p, uint16_t size,uint8_t xCol,uint8_t yPage)
{
	uint16_t j=0;
 
      // set the Column and Page addresses to 0,0
      setColAddress(xCol);
      setPageAddress(yPage);
       
         
      for(j=0;j<size;j++)
      {
    
        I2C0WriteRegister(slave_id, 0x40, *p); // Writes 0x00 to data config register (0x0E).  This is 2g range, no high pass data filter
        p++;
      }

}
void clearDisplay(void)
{
	uint16_t j=0;
 
      // set the Column and Page addresses to 0,0
      setColAddress(0);
      setPageAddress(0);
       
         
      for(j=0;j<1024;j++)
      {
    
        I2C0WriteRegister(slave_id, 0x40, 0); // 

      }

}

//  Set screen location from 0,0 upper right to 127,7 lower left
void setLocation(uint16_t xCol,uint16_t yPage){ 
	      // set the Column and Page addresses to 0,0
	      setColAddress(xCol);
	      setPageAddress(yPage);
	      }

/*  This function will transfer the shape of each ascii character to the display.  Each character
 * is a matrix of points 5x7 dots,  The pattern for each character is given in the global array  
 * asciiChar[].  Ex. the pattern for a '0' = 48 = 0x30 is the row with values
 * 
 * 	0x3E, 0x51, 0x49, 0x45, 0x3E,  map these binary values vertically and you get an ascii 0
 *   -     x     x     x     -
 *   x     -     -     -     x
 *   x     x     -     -     x
 *   x     -     x     -     x
 *   x     -     -     x     x
 *   x     -     -     -     x
 *   -     x     x     x     -
 *     */
void printChar(uint8_t c)
{
	uint16_t j=0,ct;
       ct=5*c;
       I2C0WriteRegister(slave_id, 0x40, 0); //some space before and after each character
      for(j=0;j<5;j++)
      {
    
        I2C0WriteRegister(slave_id, 0x40, asciiChar[ct]); 
        ct++;
      }
      I2C0WriteRegister(slave_id, 0x40, 0); //some space before and after each character

}
