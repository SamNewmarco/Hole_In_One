// Used when doing Horizontal or Vertical Addressing
void setColAddress(uint16_t xCol)
{
	if(xCol>(SSD1306_LCDWIDTH-1))return;
  ssd1306_command(SSD1306_COLUMNADDR); // 0x21 COMMAND
  ssd1306_command(xCol); // Column start address
  ssd1306_command(SSD1306_LCDWIDTH-1); // Column end address  0 thru 127
}
/////////////////////////////////////////////////////
// Used when doing Horizontal or Vertical Addressing
void setPageAddress(uint16_t yPage)
{  if(yPage>(SSD1306_LCDHEIGHT/8-1))return;
  ssd1306_command(SSD1306_PAGEADDR); // 0x22 COMMAND
  ssd1306_command(yPage); // Start Page address
  ssd1306_command((SSD1306_LCDHEIGHT/8)-1);// End Page address 0-7   (0 thru(64/8)-1) 
}
